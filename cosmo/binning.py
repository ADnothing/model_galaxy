def plot_binned(x, y, bins = None, range = None, levels = None, log = None, **kwargs):
    """ Usage : plot_binned(x[ii], y[ii], bins= (50, 50),range = [[8.,12.], [-5.4,0.2]], log = True, levels =(200, 100, 30, 10))"""

    if bins is None : bins = (50, 50)
    #if levels is None : levels = (1.0e4, 1.0e3, 1.0e2, 2.0e1, 2)
    if levels is None : levels = (1, 1e1, 1e2, 1e3, 1e4)
    
    H, xedges, yedges = np.histogram2d(x, y, range=range, bins=bins)
    H = np.transpose(H)
    extent = [xedges[0], xedges[-1],yedges[0], yedges[-1]]
    plt.subplots_adjust(bottom=0.15, left=0.15)
    if log:
        plt.imshow(np.log10(H),extent = extent, interpolation='nearest', aspect='auto',origin='lower', **kwargs)
    else:
        plt.imshow(H,extent = extent,interpolation='nearest', aspect='auto',origin='lower', **kwargs)
        
    plt.colorbar()
    cset = plt.contour(H, levels, origin='lower', extent=extent, \
                       colors=['black','green','blue','red', 'black'],\
                       linewidths=(1.9, 1.6, 1.5, 1.4, 1.2),)
    
    plt.clabel(cset, inline=1, fontsize=10, fmt='%1.0i')
    for c in cset.collections:
        c.set_linestyle('solid')

